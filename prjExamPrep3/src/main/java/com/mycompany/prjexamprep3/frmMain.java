    private void callSnakes() {
      
        // only if load from text file
        try (BufferedReader br = new BufferedReader(new FileReader("snakes.txt"))) {
            String line;
            int lineCount = 0;
            int nextTarget = 1; // start from the first line

            while ((line = br.readLine()) != null) {
                lineCount++;

                if (lineCount == nextTarget) {
                    cmbSnake.addItem(line.trim());
                    nextTarget += 3; // then every 3rd line after that
                }
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,
                    "Error reading file: " + ex.getMessage(),
                    "File Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

