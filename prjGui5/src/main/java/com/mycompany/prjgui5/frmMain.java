        public void Save(){
    try{
        
       
        File savefile = new File ("Products.txt"); //File object to save to our file
        
        if(!savefile.exists()){
            savefile.createNewFile();
            
        }
        
        FileWriter fw = new FileWriter(savefile.getAbsoluteFile(),true);
        
        BufferedWriter bw = new BufferedWriter(fw);
        
        bw.write("********************************");
        bw.write("Total Sales: " + prjProductSales.GetTotalSales());
        bw.newLine();
        bw.write("Average Sales: " + prjProductSales.GetAverageSales());
        bw.write("");
        bw.write("Sales over Limit: " + prjProductSales.GetSalesOverLimit());
        bw.newLine();
        bw.write("Sales under Limit: " + prjProductSales.GetSalesUnderlimit());
        bw.newLine();
        bw.write("********************************");
        bw.newLine();
        bw.close();
        
        JOptionPane.showMessageDialog(null, "Your data HAS been saved successfully!");
         
    }
     catch(IOException ex){
         JOptionPane.showMessageDialog(null, "There was an error saving to text file!");  
     }
    catch(Exception ex)
     {
         JOptionPane.showMessageDialog(null, "The following error occured" + ex.toString());
         
     }
    
    
    }//GEN-LAST:event_btnSaveActionPerformed

