    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        // TODO add your handling code here:
        listProducts.clear(); //clear list before loading again
        this.lblProductFound.setText("PRODUCTS FOUND: ");
        int intCount = 0;
        try{
            String strCurrentLine;
            BufferedReader br = new BufferedReader(new FileReader("Products.txt"));
            while ((strCurrentLine = br.readLine()) !=null){
                listProducts.addElement (strCurrentLine);
                strCurrentLine = br.readLine();
                listProducts.addElement(strCurrentLine);
                strCurrentLine=br.readLine();
                listProducts.addElement(strCurrentLine);
                strCurrentLine=br.readLine();
                listProducts.addElement(strCurrentLine);
                intCount += 1;
            }
            this.lstProducts.setModel(listProducts);
            this.lblProductFound.setText(this.lblProductFound.getText() + " " + (intCount));
        }
        catch (IOException ex){
            JOptionPane.showMessageDialog(null, "The following error occurred: " + ex.toString());
            
        }
    }//GEN-LAST:event_btnLoadActionPerformed

